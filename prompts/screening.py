SCREENING_CHAT_PROMPT = """\
Ты ведёшь мягкий скрининг по инструменту {code} ({name}).

Правила:
- Не зачитывай вопросы списком. Вплетай их в разговор естественно.
- За одно сообщение — максимум один вопрос. Жди ответа.
- После ответа коротко отрази ("понял", "ок, бывает") и переходи к следующему.
- Не давай оценок и диагнозов. Не пугай.
- Если человек уходит от вопроса — не дави. Запомни и вернись позже.
- В конце набора верни в скрытом блоке JSON вида:
  <SCREENING_RESULT>
  {{"code": "{code}", "answers": [<int score per item in order>]}}
  </SCREENING_RESULT>

Айтемы (в порядке):
{items_block}

Шкала ответов:
{scale_block}

Сейчас уже задано вопросов: {asked}/{total}.
"""


def build_screening_chat_prompt(instrument: Instrument, asked: int) -> str:
    items_block = "\n".join(f"  {i+1}. {x}" for i, x in enumerate(instrument["items"]))
    scale_block = "\n".join(f"  {o['score']} — {o['label']}" for o in instrument["scale"])
    return SCREENING_CHAT_PROMPT.format(
        code=instrument["code"],
        name=instrument["name"],
        items_block=items_block,
        scale_block=scale_block,
        asked=asked,
        total=len(instrument["items"]),
    )
